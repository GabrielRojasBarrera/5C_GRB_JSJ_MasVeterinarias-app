class Veterinaria{
   int id;
   int usuarioId;
   String descripcion;
   String imagen; 
   String representante; 
   String horaApertura;
   String horaCierre; 
   String diasLaborales;
   String nombre;
   String telefono;
   String direccion;

   Veterinaria(id, usuarioId, descripcion, imagen, representante, horaApertura, horaCierre, diasLaborales, nombre, telefono, direccion ){
     this.id = id;
     this.usuarioId = usuarioId;
     this.descripcion = descripcion;
     this.imagen = imagen;
     this.representante = representante;
     this.horaApertura = horaApertura;
     this.horaCierre = horaCierre;
     this.diasLaborales = diasLaborales;
     this.nombre = nombre;
     this.telefono = telefono;
     this.direccion = direccion;
     
   }
}

      